    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        double r1 = Double.parseDouble(txtResistenciaUno.getText());
        double r2 = Double.parseDouble(txtResistenciaDos.getText());
        double r3 = Double.parseDouble(txtResistenciaTres.getText());
        
        Calculos calc = new Calculos(r1, r2, r3);
        double rt = calc.ResistenciaCircuitoParalelo();
        double voltaje = Double.parseDouble(txtVoltaje.getText());
        double intensidad = voltaje/rt;
        txtResistenciaTotal.setText(String.valueOf(decimal.format(rt))+" Ohms");
        txtIntensidad.setText(String.valueOf(decimal.format(intensidad))+" Amp");
    }//GEN-LAST:event_jButton1ActionPerformed

